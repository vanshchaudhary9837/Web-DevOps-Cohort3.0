// 1. Write an if/else statement that checks if a number is even or odd. 
// If it's even, print "The number is even." Otherwise, print "The number is odd."


// declare a variable `num` and assign it a value of 5
let num = 5;

// if the number is divisible by 2, it's the number is even else it's odd
if (num % 2 === 0) {
    console.log("The number is even.");
} else {
    console.log("The number is odd.");
}